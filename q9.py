x = int(input("Enter a number:"))
if x == 1:
   print("Monday.")
elif x == 2:
   print("Tuesday.")
elif x == 3:
   print("Wednesday.")
elif x == 4:
   print("Thursday.")
elif x == 5:
   print("Friday.")
elif x == 6:
   print("Saturday.")
elif x == 7:
   print("Sunday.")
else:
   print("Please enter a number from 1 - 7.")

